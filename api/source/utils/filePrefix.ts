enum FilePrefix {
  ATTRACTION = 'attraction',
  GROUP = 'group',
  AVATAR = 'avatar',
  JOURNEY = 'journey',
  DOCUMENT = 'document'
}

export const attractionPrefix = () => {
  return FilePrefix.ATTRACTION;
};

export const groupPrefix = () => {
  return FilePrefix.GROUP;
};

export const avatarPrefix = () => {
  return FilePrefix.AVATAR;
};

export const journeyPrefix = () => {
  return FilePrefix.JOURNEY;
};

export const documentPrefix = () => {
  return FilePrefix.DOCUMENT;
};
